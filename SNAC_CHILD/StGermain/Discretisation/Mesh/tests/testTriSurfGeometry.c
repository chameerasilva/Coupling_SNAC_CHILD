#include <mpi.h>
#include "Base/Base.h"

#include "Discretisation/Geometry/Geometry.h"
#include "Discretisation/Shape/Shape.h"
#include "Discretisation/Mesh/Mesh.h"

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <mpi.h>

int main( int argc, char *argv[] ) {
	MPI_Comm CommWorld;
	int rank;
	int numProcessors;
	int procToWatch;
	
	/* Initialise MPI, get world info */
	MPI_Init( &argc, &argv );
	MPI_Comm_dup( MPI_COMM_WORLD, &CommWorld );
	MPI_Comm_size( CommWorld, &numProcessors );
	MPI_Comm_rank( CommWorld, &rank );
	
	Base_Init( &argc, &argv );
	
	DiscretisationGeometry_Init( &argc, &argv );
	DiscretisationShape_Init( &argc, &argv );
	DiscretisationMesh_Init( &argc, &argv );
	MPI_Barrier( CommWorld ); /* Ensures copyright info always come first in output */
	
	if( argc >= 2 ) {
		procToWatch = atoi( argv[1] );
	}
	else {
		procToWatch = 0;
	}
	if( rank == procToWatch ) {
		Dictionary*	dictionary;
		MeshGeometry*	mg;
		Coord		coord;
		Index		i;
		XML_IO_Handler*	io_handler = XML_IO_Handler_New();

		/* Read input */
		dictionary = Dictionary_New();
		IO_Handler_ReadAllFromFile(io_handler, "data/surface.xml", dictionary);

		mg = (MeshGeometry*)TriSurfGeometry_New(dictionary, "imNodes");
		Print(mg);

		for (i = 0; i < mg->nodeCount; i++)
		{
			printf("Node %u : ", i);
			mg->nodeAt(mg, i, coord);
			printf("{%lf, %lf, %lf}\n", coord[0], coord[1], coord[2]);
		}
		printf("\n");

		Stg_Class_Delete(mg);

		/* Destroy stuff */
		Stg_Class_Delete(dictionary);
	}
	
	DiscretisationMesh_Finalise();
	DiscretisationShape_Finalise();
	DiscretisationGeometry_Finalise();
	
	Base_Finalise();
	
	/* Close off MPI */
	MPI_Finalize();
	
	return 0; /* success */
}
